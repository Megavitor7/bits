let age = prompt("What is your age?");

switch (age) {
    case "18":
        alert("You are an adult");
        break;
    case "25":
        alert("A quarter of a century is something to celebrate!");
        break;
    case "50":
        alert("Half a century is something!");
        break;
    default:
        alert("I don't know what to say based on your age");
        break
}
